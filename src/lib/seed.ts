        queue_cycle: p.queue_cycle,
        utility: p.utility,
        poi: p.poi,
        poi_voltage: p.poi_voltage,
        project_manager: p.project_manager,
        admin_emails: p.admin_emails,
        developer_emails: p.developer_emails,
        interconnection_status: p.interconnection_status,
