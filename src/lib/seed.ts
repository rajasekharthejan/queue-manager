        suspension: p.suspension,
        provisional_ia_status: p.provisional_ia_status,
        ssr_status: p.ssr_status || null,
        security_posted_to_date: p.security_posted_to_date,
        security_at_risk_to_date: p.security_at_risk_to_date,
        study_deposits_posted: p.study_deposits_posted,
        sunk_cost_to_date: p.sunk_cost_to_date,
        interconnection_facilities_cost: p.interconnection_facilities_cost,
        grid_network_upgrades: p.grid_network_upgrades,
        affected_system_costs: p.affected_system_costs,
        affected_system: p.affected_system,
        lgia_security: p.lgia_security,
        next_security_milestone: p.next_security_milestone,
        qsa_deadline: p.qsa_deadline,
        next_due_date: p.next_due_date,
        planned_cod: p.planned_cod,
        feasibility_study_status: p.feasibility_study_status || "Not Started",
        system_impact_study_status: p.system_impact_study_status || "Not Started",
        facilities_study_status: p.facilities_study_status || "Not Started",
